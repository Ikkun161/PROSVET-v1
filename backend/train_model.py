def preprocess_russian_text(text):
    if not isinstance(text, str):
        return ""
    text = text.lower()
    text = re.sub(r'http\S+|www\S+|https\S+', '', text, flags=re.MULTILINE)
    text = re.sub(r'[^а-яёa-z0-9\s\-/]', ' ', text)
    text = re.sub(r'\s+', ' ', text).strip()
    tokens = text.split()
    cleaned = []
    for token in tokens:
        if len(token) <= 1:
            continue
        if re.search(r'[a-z]', token) or token in BUSINESS_TERMS:
            if token not in STOP_WORDS_RU:
                cleaned.append(token)
        else:
            if token not in STOP_WORDS_RU:
                try:
                    lemma = morph.parse(token)[0].normal_form
                    cleaned.append(lemma)
                except:
                    cleaned.append(token)
    return ' '.join(cleaned)

def generate_variations(texts, num_variations=3):
    variations = []
    replacements = {
        'требуется': ['ищем', 'требуется', 'открыта вакансия', 'приглашаем', 'нужен'],
        'опыт': ['опыт', 'стаж', 'практика', 'навыки'],
        'обязательно': ['обязательно', 'необходимо', 'требуется', 'нужно'],
        'приветствуется': ['приветствуется', 'будет плюсом', 'желательно', 'плюсом будет'],
        'знание': ['знание', 'владение', 'понимание', 'опыт работы с'],
        'работа': ['работа', 'деятельность', 'занятость', 'позиция'],
        'задачи': ['задачи', 'обязанности', 'функции', 'зона ответственности'],
        'команда': ['команда', 'коллектив', 'отдел', 'департамент'],
        'компания': ['компания', 'организация', 'фирма', 'предприятие', 'холдинг'],
        'крупный': ['крупный', 'большой', 'ведущий', 'топовый', 'федеральный'],
    }
    for text in texts:
        variations.append(text)
        for i in range(num_variations - 1):
            varied_text = text
            for word, synonyms in replacements.items():
                if word in varied_text.lower():
                    if np.random.random() > 0.5:
                        synonym = np.random.choice(synonyms)
                        varied_text = re.sub(word, synonym, varied_text, flags=re.IGNORECASE)
            variations.append(varied_text)
    return variations

