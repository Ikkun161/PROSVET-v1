def load_training_data(filepath='training_data.csv'):
    try:
        df = pd.read_csv(filepath, encoding='utf-8')
        return df
    except FileNotFoundError:
        return create_synthetic_dataset()

def train_model(df):
    print("Предобработка текстов...")
    df['clean_text'] = df['description'].apply(preprocess_russian_text)
    X_train, X_test, y_train, y_test = train_test_split(
        df['clean_text'], df['label'], test_size=0.2, random_state=42, stratify=df['label']
    )
    pipeline = Pipeline([
        ('tfidf', TfidfVectorizer(max_features=5000, ngram_range=(1, 3), min_df=2, max_df=0.8, sublinear_tf=True)),
        ('clf', LogisticRegression(solver='lbfgs', max_iter=1000, random_state=42, class_weight='balanced'))
    ])
    param_grid = {
        'tfidf__max_features': [3000, 5000],
        'tfidf__ngram_range': [(1, 2), (1, 3)],
        'clf__C': [0.1, 1.0, 10.0]
    }
    grid_search = GridSearchCV(pipeline, param_grid, cv=3, scoring='accuracy', n_jobs=-1, verbose=1)
    grid_search.fit(X_train, y_train)
    best_model = grid_search.best_estimator_
    print("Лучшие параметры:", grid_search.best_params_)
    y_pred = best_model.predict(X_test)
    print("Точность на тесте:", accuracy_score(y_test, y_pred))
    return best_model

